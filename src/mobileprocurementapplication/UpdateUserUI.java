    public UpdateUserUI(int userID, String userName) {
        initComponents();
        
        //Setting the username
        Username = userName;
        
        //Setting the userID 
        UserID = userID;
        
        //Setting the initial position of the window to center
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
        
        userService = new UserService();
        
        try {
            userList = this.userService.Deserialize(UserFile);
        } catch (IOException ex) {
            Logger.getLogger(UpdateUserUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateUserUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for(User aUser : userList)
        {
            if(aUser.getUserID() == UserID)
            {
                txtFullName.setText(aUser.getUserFullName());
                txtTelephoneNumber.setText(aUser.getTelephoneNumber());
                txtUsername.setText(aUser.getUsername());
                
                if(aUser.getUserType().equals("Site Manager"))
                {
                    cmbUserType.setSelectedIndex(0);
                }
                else if(aUser.getUserType().equals("Accountant"))
                {
                    cmbUserType.setSelectedIndex(1);
                }
                else if(aUser.getUserType().equals("Manager"))
                {
                    cmbUserType.setSelectedIndex(2);
                }     
                
                break;
            }           
        }
        
        disableInterface();
                
    }
    
