    private void btnUpdateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateUserActionPerformed

        //Updating the user

        if(txtFullName.getText().equals("") || txtTelephoneNumber.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please fill all text boxes!", null, JOptionPane.ERROR_MESSAGE);
        }        
        else
        {
                        
            for(User aUser : userList)
            {
                if(aUser.getUserID() == UserID)
                {
                    User updatedUser = new User (UserID, txtFullName.getText(), txtTelephoneNumber.getText(), txtUsername.getText(), aUser.getPassword(), cmbUserType.getSelectedItem().toString());
                    userList.remove(aUser);
                    
                    try {
                        this.userService.Serialize(userList, UserFile);
                    } catch (IOException ex) {
                        Logger.getLogger(UpdateUserUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    userList.add(updatedUser);
                    
                    try {
                        this.userService.Serialize(userList, UserFile);
                        JOptionPane.showMessageDialog(null, "User Successfully Update", null, JOptionPane.INFORMATION_MESSAGE);                         
                        disableInterface();
                    } catch (IOException ex) {
                        Logger.getLogger(UpdateUserUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                    break;
                }
            }      

        }

    }//GEN-LAST:event_btnUpdateUserActionPerformed

